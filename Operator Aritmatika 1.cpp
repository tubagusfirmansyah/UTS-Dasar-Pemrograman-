#include <iostream>
using namespace std;

int main()
{
int x = 82, y = 26;
cout << x << "+" << y <<" = "<<x+y << endl;
cout << x << "_" << y <<" = "<<x-y << endl;
cout << x << "*" << y <<" = "<<x*y << endl;
cout << x << "/" << y <<" = "<<x/y << endl;
cout << x << "%" << y <<" = "<<x%y << endl;
cout <<"_"  << x << -x << endl;

return 0;
}
